*--- Some ASCII Characters ---

End		EQU	$00		; End Of String
NUL		EQU	$00		; Null character		( ^@ )
SOH		EQU	$01		; Start Of Heading		( ^A )
STX		EQU	$02		; Start Of Text			( ^B )
ETX		EQU	$03		; End Of Text			( ^C )
EOT		EQU	$04		; End Of Transmission		( ^D )
EOL		EQU	$04		; End Of Line			( ^D )
BEL		EQU	$07		; Bell				( ^G )
BS		EQU	$08		; Back Space			( ^H )
TAB		EQU	$09		; Horizontal TABulator		( ^I )
LF		EQU	$0A		; Line Feed			( ^J )
CR		EQU	$0D		; Carrige Return		( ^M )
DLE		EQU	$10		; DATA LINK ESCAPE		( ^P )
XON		EQU	$11		; Hanshake Xmit On		( ^Q )
XOFF		EQU	$13		; Handshake Xmit Off		( ^S )
CAN		EQU	$18		; Cancel			( ^X )
ESC		EQU	$1B		; Escape			( ^[ )
SPACE		EQU	$20		; Space
SP		EQU	$20		; Space
DEL		EQU	$7F		; Delete

